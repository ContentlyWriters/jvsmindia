export async function generateMetadata({ params }) {

  const { slug } = await params;


  const post = blogData.find((blog) => blog.slug === slug);

  if (!post) {
    return notFound();
  }


  return {
    title: post.heading, 
    description: post.description, 
    keywords: post.keywords.join(", "), 
  };
}


